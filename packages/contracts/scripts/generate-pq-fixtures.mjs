/**
 * Generate ETHFALCON test fixtures for Forge fork tests.
 *
 * Uses bedrock-wasm to generate ETHFALCON keypairs and signatures,
 * then outputs a Solidity file with hex constants.
 *
 * The raw ZKNOX verify(h, salt, s2[], ntth[]) expects:
 *   - salt: 40 bytes (from internal signature bytes[1..41])
 *   - s2: 1024 bytes = 32 uint256 values (from toEthFalconSignature)
 *
 * Usage: node scripts/generate-pq-fixtures.mjs
 */

import { initBedrock, createModule, FalconScheme } from '@tectonic-labs/bedrock-wasm';
import { bytesToHex, hexToBytes, toHex } from 'viem';
import { writeFileSync } from 'node:fs';
import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const OUTPUT_PATH = resolve(__dirname, '../test/fixtures/PQFixtures.sol');

async function main() {
  console.log('Initializing bedrock-wasm...');
  const bedrock = await initBedrock(createModule());

  // Deterministic seed (48 bytes for FN-DSA)
  const seed = new Uint8Array(48);
  seed.fill(0x42);

  console.log('Generating ETHFALCON keypair from seed...');
  const keypair = bedrock.generateFnDsaKeypairFromSeed(FalconScheme.ETH_FALCON, seed);

  // Message hash (32 bytes)
  const messageHash = hexToBytes('0xa1b2c3d4e5f6a1b2c3d4e5f6a1b2c3d4e5f6a1b2c3d4e5f6a1b2c3d4e5f6a1b2');

  console.log('Signing message hash with ETHFALCON...');
  const signature = bedrock.signFnDsa(keypair.secret_key, messageHash);

  const isValid = bedrock.verifyFnDsa(keypair.public_key, signature, messageHash);
  if (!isValid) throw new Error('Local verification failed');
  console.log('Local verification passed.');

  // Convert to Solidity format
  const solidityPk = bedrock.toEthFalconVerifyingKey(keypair.public_key);
  const soliditySig = bedrock.toEthFalconSignature(signature);

  // Extract salt from internal signature (bytes 1..41, skip 1-byte header)
  const internalSigBytes = hexToBytes(`0x${signature.value}`);
  const salt = internalSigBytes.slice(1, 41);

  // Full signature: salt(40) + s2(1024) = 1064 bytes
  const fullSig = new Uint8Array(salt.length + soliditySig.length);
  fullSig.set(salt, 0);
  fullSig.set(soliditySig, salt.length);

  console.log(`Public key: ${solidityPk.length} bytes`);
  console.log(`Full signature (salt + s2): ${fullSig.length} bytes`);

  const pkHex = bytesToHex(solidityPk);
  const fullSigHex = bytesToHex(fullSig);
  const messageHashHex = toHex(messageHash);

  const solidity = `// SPDX-License-Identifier: MIT
pragma solidity ^0.8.30;

/// @title PQFixtures
/// @notice Test fixtures generated by scripts/generate-pq-fixtures.mjs
/// @dev Regenerate with: node scripts/generate-pq-fixtures.mjs
///
/// ETHFALCON keypair generated from deterministic seed (48 bytes of 0x42).
/// Signature is over MESSAGE_HASH (32 bytes) using ETHFALCON scheme.
/// Public key is in Solidity format (1024 bytes = uint256[32]).
/// Signature format: salt(40) + s2(1024) = 1064 bytes.
library PQFixtures {
    function messageHash() internal pure returns (bytes32) {
        return ${messageHashHex};
    }

    /// @notice ETHFALCON public key (1024 bytes = abi.encodePacked(uint256[32]))
    function ethfalconPublicKey() internal pure returns (bytes memory) {
        return hex"${pkHex.slice(2)}";
    }

    /// @notice ETHFALCON signature: salt(40) + s2(1024) = 1064 bytes
    function ethfalconSignature() internal pure returns (bytes memory) {
        return hex"${fullSigHex.slice(2)}";
    }
}
`;

  writeFileSync(OUTPUT_PATH, solidity);
  console.log(`Fixtures written to ${OUTPUT_PATH}`);
}

main().catch(err => {
  console.error(err);
  process.exit(1);
});
